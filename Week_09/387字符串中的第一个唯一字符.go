func firstUniqChar(s string) int {
	set := [26]int{}

	for _ , char := range s {
		set[char - 'a'] += 1
	}

	for idx, char	:= range s {
		if set[char- 'a'] == 1 {
			return idx
		}
	}

	return  -1
}
